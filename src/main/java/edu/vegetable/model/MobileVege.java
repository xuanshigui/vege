package edu.vegetable.model;

public class MobileVege {

    private Integer vegeId;

    private String vegeName;

    private String imgPath;

    public Integer getVegeId() {
        return vegeId;
    }

    public void setVegeId(Integer vegeId) {
        this.vegeId = vegeId;
    }

    public String getVegeName() {
        return vegeName;
    }

    public void setVegeName(String vegeName) {
        this.vegeName = vegeName;
    }

    public String getImgPath() {
        return imgPath;
    }

    public void setImgPath(String imgPath) {
        this.imgPath = imgPath;
    }
}
